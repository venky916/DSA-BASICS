# Dynamic Programming (DP) is a technique to solve problems by breaking them down 
# into overlapping subproblems, solving each subproblem once, 
# and storing the results for future use (memoization or tabulation).

# Key Concepts:
# 1 Overlapping Subproblems: Problems can be divided into smaller problems that are solved multiple times.
# 2 Optimal Substructure: The solution to a problem can be constructed from the solutions of its subproblems.
# 3 Memoization: Top-down approach using recursion with a cache to store intermediate results.
# 4 Tabulation: Bottom-up approach using an iterative table to build solutions.